package demo.demo.requestbody;

public class OvertimeInfo {
	
	private int id;
	private String datetime;
	
	public OvertimeInfo() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDatetime() {
		return datetime;
	}

	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}

}
