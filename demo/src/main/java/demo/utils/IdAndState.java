package demo.utils;

public class IdAndState {
	
	private int id;
	private String state;
	
	public IdAndState() {}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getState() {
		return state;
	}
	
	public void setState(String state) {
		this.state = state;
	}

}
